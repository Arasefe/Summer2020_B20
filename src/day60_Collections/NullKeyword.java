package day60_Collections;

public class NullKeyword {
    public static void main(String[] args) {

        String s1 = "Cybertek";

        System.out.println( s1.toUpperCase() );

        String s2 = null;

        System.out.println( s2.toUpperCase() );


        Integer i=new Integer(5);
        System.out.println("i= "+i);
         i=null;
        System.out.println("i= "+i);

    }
}
